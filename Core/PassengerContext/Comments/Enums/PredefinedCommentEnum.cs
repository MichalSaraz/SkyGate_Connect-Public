namespace Core.PassengerContext.Comments.Enums;

/// <summary>
/// Represents predefined comments that can be associated with a booking.
/// </summary>
public enum PredefinedCommentEnum
{
    /// <summary>
    /// Indicates requirements of additional document checks.
